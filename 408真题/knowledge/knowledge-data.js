  const byId = new Map(KNOWLEDGE_DATA.map((item) => [item.id, item]));

  function findKnowledgeRefs(question) {
    const subject = question && question.subject;
    const topic = String(question?.topic || "").toLowerCase();
    const body = String(question?.question || "").toLowerCase();
    const candidates = KNOWLEDGE_DATA.filter((item) => item.subject === subject).map((item) => {
      let score = 0;
      item.keywords.forEach((keyword) => {
        const normalized = String(keyword).toLowerCase();
        if (topic.includes(normalized)) score += normalized.length * 4 + 8;
        else if (body.includes(normalized)) score += normalized.length + 1;
      });
      if (topic.includes(item.title.toLowerCase())) score += 30;
      return { item, score };
    }).filter((entry) => entry.score > 0).sort((a, b) => b.score - a.score);

    const refs = [];
    candidates.forEach(({ item, score }) => {
      if (refs.length < 3 && (refs.length === 0 || score >= 4)) refs.push(item);
    });
    if (!refs.length && SUBJECT_META[subject]) refs.push(byId.get(SUBJECT_META[subject].fallback));
    return refs.filter(Boolean);
  }

  window.KNOWLEDGE_DATA = KNOWLEDGE_DATA;
  window.KNOWLEDGE_SUBJECTS = SUBJECT_META;
  window.KNOWLEDGE_BY_ID = byId;
  window.findKnowledgeRefs = findKnowledgeRefs;
